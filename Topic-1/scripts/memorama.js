                '<img src="./Topic-1/img/modulo1/memorama/nivel2/' + Datos7[0].memo[0].opciones[(i - 1)].op2 + '.png" alt="imagen de apoyo" aria-hidden="true" />' +
                '<i class="sr-only">' + Datos7[0].memo[0].opciones[(i - 1)].op3 + '</i>' +
                '</button>';
            cartas.push(elemento, elemento2);
